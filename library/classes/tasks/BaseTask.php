<?php
namespace Library\Classes\Tasks;

use Library\Main\AbstractTask;

class BaseTask implements AbstractTask
{
    private $status;

    protected $name = 'task';

    public function process()
    {
        return true;
    }

    /**
     * @return int
     */
    public function getStatus()
    {
        return $this->status;
    }

    /**
     * @param int $status
     */
    public function setStatus($status)
    {
        $this->status = $status;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }


}